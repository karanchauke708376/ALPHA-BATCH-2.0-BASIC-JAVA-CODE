package Operators;

public class Relational_Operators {

    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int c = 10;

        System.out.println("a == b "+( a == b ));
        System.out.println("a == c "+( a == c ));
        System.out.println("--------------------------");

        int a1 = 10;
        int b1 = 5;
        int c1 = 10;

        System.out.println("a1 != b1 "+( a1 != b1 ));
        System.out.println("a1 != c1 "+( a1 != c1 ));
        System.out.println("--------------------------");

        int a3 = 10;
        int b3 = 5;
        int c3 = 50;

        System.out.println("a3 > b3 "+( a3 > b3 ));
        System.out.println("a3 > c3 "+( a3 > c3 ));
        System.out.println("--------------------------");

        int a4 = 10;
        int b4 = 5;
        int c4 = 50;

        System.out.println("a4 < b4 "+( a4 < b4 ));
        System.out.println("a4 < c4 "+( a4 < c4 ));
        System.out.println("--------------------------");

        int a5 = 10;
        int b5 = 50;
        int c5 = 10;

        System.out.println("a5 >= b5 "+( a5 >= b5 ));
        System.out.println("a5 >= c5 "+( a5 >= c5 ));
        System.out.println("--------------------------");

        int a6 = 20;
        int b6 = 20;
        int c6 = 50;

        System.out.println("a6 <= b6 "+( a6 >= b6 ));
        System.out.println("a6 <= c6 "+( a6 >= c6 ));
        System.out.println("--------------------------");
    }
}
